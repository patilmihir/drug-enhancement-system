    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed

        int selectedRow = tblWorkRequest.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } 
        String status =String.valueOf( tblWorkRequest.getValueAt(selectedRow, 3));
        if(status.equals("Completed")) {
            JOptionPane.showMessageDialog(this, "This request is already processed.", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        ProcureCompoundWorkRequest request = (ProcureCompoundWorkRequest) tblWorkRequest.getValueAt(selectedRow, 0);
        request.setStatus("Processing");
        ProcessRequestJPanel processRequestJPanel = new ProcessRequestJPanel(userProcessContainer, request,drugManufacturingEnterprise);
        userProcessContainer.add("processRequestJPanel", processRequestJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_btnProcessActionPerformed

