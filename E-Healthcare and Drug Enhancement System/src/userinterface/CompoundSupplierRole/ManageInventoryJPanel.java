    public void populateCompoundCatalog() {
        DefaultTableModel model = (DefaultTableModel) tblCompoundCatalog.getModel();
        model.setRowCount(0);
        for (Compound compound : drugManufacturingEnterprise.getMasterCompoundCatalog().getCompoundList()) {
            Object row[] = new Object[4];
            row[0] = compound.getId();
            row[1] = compound;
            row[2] = compound.getMolecularFormula();
            row[3] = compound.getQuantity();
            model.addRow(row);
        }
    }

