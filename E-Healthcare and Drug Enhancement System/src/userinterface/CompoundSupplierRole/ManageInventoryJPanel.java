    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCompoundCatalog.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table");
            return;
        }
        Compound selectedCompound = (Compound) tblCompoundCatalog.getValueAt(selectedRow, 1);
        UpdateCompoundJPanel ucjp = new UpdateCompoundJPanel(userProcessContainer, drugManufacturingEnterprise, selectedCompound);
        userProcessContainer.add("UpdateCompoundJPanel", ucjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCompoundCatalog.getSelectedRow();
        if (selectedRow >= 0) {
            Compound compound = (Compound) tblCompoundCatalog.getValueAt(selectedRow, tblCompoundCatalog.getColumn("Name").getModelIndex());
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete?", "Warning", JOptionPane.YES_NO_OPTION);
            if (dialogResult == 0) {
                drugManufacturingEnterprise.getMasterCompoundCatalog().deleteCompound(compound);
                populateCompoundCatalog();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

