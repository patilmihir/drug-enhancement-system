    private void FilterTable() {
        final List<RowFilter<TableModel, Object>> filters = new ArrayList<RowFilter<TableModel, Object>>();
        if (firstFilter != null) {
            filters.add(firstFilter);
        }
        if (secondFilter != null) {
            filters.add(secondFilter);
        }
        if (thirdFilter != null) {
            filters.add(thirdFilter);
        }
        if (fourthFilter != null) {
            filters.add(fourthFilter);
        }
        final RowFilter<TableModel, Object> compoundRowFilter = RowFilter.andFilter(filters);
        TableRowSorter<DefaultTableModel> rowSorter = new TableRowSorter<DefaultTableModel>((DefaultTableModel) tblCompoundCatalog.getModel());
        rowSorter.setRowFilter(compoundRowFilter);
        tblCompoundCatalog.setRowSorter(rowSorter);
    }

