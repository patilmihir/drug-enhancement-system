    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        if (validateData()) {
            String name = txtName.getText();
            String gene = txtGene.getText();
            String severity = txtSeverity.getText();
            Disease.category category = (Disease.category) cmbCategory.getSelectedItem();

            Disease disease = system.getDiseaseDirectory().addDisease(name, gene, severity, category, false);
            populateDiseaseTable();
            resetFields();
        }
    }//GEN-LAST:event_submitJButtonActionPerformed

