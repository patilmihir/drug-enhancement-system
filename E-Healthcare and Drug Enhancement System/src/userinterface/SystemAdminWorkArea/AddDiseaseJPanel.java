    public boolean validateData() {
        if (txtName.getText().trim().equals("") || txtGene.getText().trim().equals("") || txtSeverity.getText().trim().equals("") || cmbCategory.getSelectedItem().equals("Select")) {
            JOptionPane.showMessageDialog(null, "All fields are mandatory!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if (!txtName.getText().trim().matches("^[a-zA-Z][a-zA-Z0-9 ]+")) {
            JOptionPane.showMessageDialog(null, "Please enter a valid name.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if (!txtGene.getText().trim().matches("[a-zA-Z ]+")) {
            JOptionPane.showMessageDialog(null, "Please enter a valid gene value.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (!txtSeverity.getText().trim().matches("[a-zA-Z ]+")) {
            JOptionPane.showMessageDialog(null, "Please enter a valid severity value.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }

