    public void populateCombo() {
        cmbDisease.removeAllItems();
        cmbDisease.addItem("Select");
        for (Disease disease : diseaseDirectory.getDiseaseList()) {
            cmbDisease.addItem(disease);
        }
        if(cmbDrug.getItemCount() == 1) {
            cmbDisease.removeAllItems();
        }

        
    }
    
    public void populateDrugCombo(Disease disease) {
        cmbDrug.removeAllItems();
        cmbDrug.addItem("Select");
        for (DrugItems drugItems : approvedDrugsDirectory.getApprovedDrugs()) {
            if(disease.getName().equals(drugItems.getDrug().getDisease()))
            {
                
            cmbDrug.addItem(drugItems);
            }
        }
    }
