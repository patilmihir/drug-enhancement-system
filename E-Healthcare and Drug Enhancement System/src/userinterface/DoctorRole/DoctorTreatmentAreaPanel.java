    private void btnDiagnoseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiagnoseActionPerformed
        // TODO add your handling code here:

        PrescriptionWorkRequest request = new PrescriptionWorkRequest();

        request.setDrugItems((DrugItems) cmbDrug.getSelectedItem());
        request.setSender(account);
        request.setStatus("Sent");
        request.setQuantity(Integer.parseInt(txtQuantity.getText()));

        Organization organization = null;
        UserAccount ua = null;
        for (Organization org : hospitalEnterprise.getOrganizationDirectory().getOrganizationList()) {

            if (org instanceof PatientOrganization) {
                organization = org;
                break;
            }
        }

        for (UserAccount user : ((PatientOrganization) organization).getUserAccountDirectory().getUserAccountList()) {

            if ((Patient) user.getPerson() == dew.getPatient()) {
                user.getWorkQueue().getWorkRequestList().add(request);
            }
        }
        account.getWorkQueue().getWorkRequestList().add(request);
    }//GEN-LAST:event_btnDiagnoseActionPerformed

