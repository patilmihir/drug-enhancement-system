    private void populateCombo() {
        organizationJComboBox.removeAllItems();
        for (Type type : Organization.Type.values()) {
            if (!(type.getValue().equals(Type.Admin.getValue())||type.getValue().equals(Type.Doctor.getValue())||type.getValue().equals(Type.Nurse.getValue())||type.getValue().equals(Type.Receptionist.getValue())||type.getValue().equals(Type.LocalPharmacy.getValue())||type.getValue().equals(Type.Patient.getValue()) )) {
                organizationJComboBox.addItem(type);
            }
        }
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();

        model.setRowCount(0);

        for (Organization organization : organizationDirectory.getOrganizationList()) {
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();

            model.addRow(row);
        }
    }

