    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Trial t2 = null;
        for (Trial t : drugVersions.getTrialDirectory().getTrialDirectory()) {
            if (t instanceof PhaseTwo) {
                t2 = t;
                break;
            }
        }
    
        if (phaseTwoSuccess(t2)) {
            Trial trial = new PhaseThree();
            trial.setName(txtName2.getText());
            trial.setNumber(Integer.parseInt(txtNum2.getText()));
            trial.setStartDate(jdcStart2.getDate());
            trial.setEndDate(jdcEnd2.getDate());
            ((PhaseThree) trial).setRecommendation(jTextArea1.getText());
            ((PhaseThree) trial).setSafety(Integer.parseInt(jTextField1.getText()));
            int safe = Integer.parseInt(jTextField1.getText());
            if (safe > 70 && safe <= 100) {
                ((PhaseThree) trial).setIsSafe(true);
                trial.setSuccessful(true);
            } else {
                ((PhaseThree) trial).setIsSafe(false);
                trial.setSuccessful(false);
                drugTrialWorkRequest.setStatus("Completed");
                JOptionPane.showMessageDialog(this, "Phase Three Trial has been failed", "Warning", JOptionPane.WARNING_MESSAGE);
                panelThreeDisable();
            }
            drugVersions.getTrialDirectory().getTrialDirectory().add(trial);
            panelOneDisable();
            panelTwoDisable();
            panelThreeDisable();
        } else {
            JOptionPane.showMessageDialog(this, "Drug rejected in phase two trial", "Information", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

