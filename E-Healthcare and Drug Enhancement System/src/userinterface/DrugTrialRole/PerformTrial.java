    public boolean phaseOneSuccess(Trial t) {
        boolean flag = false;
        Float max = ((PhaseOne) t).getMaximumDosage();
        Float min = ((PhaseOne) t).getMinDosage();
        int ld50 = ((PhaseOne) t).getLd50();

        if (ld50 <= ((min + max) / 2)) {
            flag = true;
        } else {
            flag = false;
        }

        t.setSuccessful(flag);
        return flag;
    }

    public boolean phaseTwoSuccess(Trial t) {
        boolean flag = false;

        int spec = ((PhaseTwo) t).getSpecimenUsed();
        int specA = ((PhaseTwo) t).getSpecimenAbvThreshold();
        int specB = ((PhaseTwo) t).getSpecimenBelowThreshold();
        int etv = ((PhaseTwo) t).getEffectivenessThreshold();

        int suc = (specA *100)/ spec;

        if (suc > 70) {
            flag = true;
        } else {
            flag = false;
            drugTrialWorkRequest.setStatus("Completed");
        }

        t.setSuccessful(flag);
        return flag;
    }

