    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Trial t1 = null;
        for (Trial t : drugVersions.getTrialDirectory().getTrialDirectory()) {
            if (t instanceof PhaseOne) {
                t1 = t;
                break;
            }
        }

        if (phaseOneSuccess(t1)) {
            Trial trial = new PhaseTwo();
            trial.setStartDate(jdcStart1.getDate());
            trial.setEndDate(jdcEnd1.getDate());
            trial.setName(txtName1.getText());
            trial.setNumber(Integer.parseInt(txtNum1.getText()));
            ((PhaseTwo) trial).setSpecimenUsed(Integer.parseInt(txtSpec.getText()));
            ((PhaseTwo) trial).setEffectivenessThreshold(Integer.parseInt(txtETV.getText()));
            ((PhaseTwo) trial).setSpecimenAbvThreshold(Integer.parseInt(txtAbove.getText()));
            ((PhaseTwo) trial).setSpecimenBelowThreshold(Integer.parseInt(txtBelow.getText()));
            if(phaseTwoSuccess(trial)){
            drugVersions.getTrialDirectory().getTrialDirectory().add(trial);
            panelTwoDisable();
            panelThreeEnable();
            } else {
                drugTrialWorkRequest.setStatus("Completed");
                JOptionPane.showMessageDialog(this, "Phase Two Trial has been failed", "Warning", JOptionPane.WARNING_MESSAGE);
                panelTwoDisable();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Drug rejected in phase one trial", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

