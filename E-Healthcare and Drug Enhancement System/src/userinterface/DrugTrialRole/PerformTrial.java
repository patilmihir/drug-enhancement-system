    public void populatePanels() {
    if(!(drugVersions.getTrialDirectory().getTrialDirectory().isEmpty())) {
        for(Trial t : drugVersions.getTrialDirectory().getTrialDirectory()) {
            if(t instanceof PhaseOne){
                jdcStart.setDate(((PhaseOne) t).getStartDate());
                jdcEnd.setDate(((PhaseOne) t).getEndDate());
                txtNum.setText(String.valueOf(((PhaseOne) t).getNumber()));
                txtName.setText(((PhaseOne) t).getName());
                txtNum3.setText(String.valueOf(((PhaseOne) t).getMinDosage()));
                txtNum4.setText(String.valueOf(((PhaseOne) t).getMaximumDosage()));
                txtNum5.setText(String.valueOf(((PhaseOne) t).getLd50()));
                txtSideEffect.setText(((PhaseOne) t).getSideEffects());
            } else if(t instanceof PhaseTwo) {
                 jdcStart1.setDate(((PhaseTwo) t).getStartDate());
                jdcEnd1.setDate(((PhaseTwo) t).getEndDate());
                txtNum1.setText(String.valueOf(((PhaseTwo) t).getNumber()));
                txtName1.setText(((PhaseTwo) t).getName());
                txtSpec.setText(String.valueOf(((PhaseTwo) t).getSpecimenUsed()));
                txtETV.setText(String.valueOf(((PhaseTwo) t).getEffectivenessThreshold()));
                txtAbove.setText(String.valueOf(((PhaseTwo) t).getSpecimenAbvThreshold()));
                txtBelow.setText(String.valueOf(((PhaseTwo) t).getSpecimenBelowThreshold()));
            } else if(t instanceof PhaseThree) {
                jdcStart2.setDate(((PhaseThree) t).getStartDate());
                jdcEnd2.setDate(((PhaseThree) t).getEndDate());
                txtNum2.setText(String.valueOf(((PhaseThree) t).getNumber()));
                txtName2.setText(((PhaseThree) t).getName());
                jTextField1.setText(String.valueOf(((PhaseThree) t).getSafety()));
                jTextArea1.setText(((PhaseThree) t).getRecommendation());
            }
        }
    }
    }
    
    public void checkTrials() {
    
        for(Trial t : drugVersions.getTrialDirectory().getTrialDirectory()) {
            if(t instanceof PhaseOne){
                panelOneDisable();
                panelTwoEnable();
                panelThreeEnable();
            } else if(t instanceof PhaseTwo) {
                panelOneDisable();
                panelTwoDisable();
                panelThreeEnable();
            } else if(t instanceof PhaseThree) {
                panelOneDisable();
                panelTwoDisable();
                panelThreeDisable();
            }
        }
    }
    
