    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblApprovedDrugs.getSelectedRow();
        if (selectedrow < 0) {
            JOptionPane.showMessageDialog(this, "Select a row.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            DrugEvaluationWorkRequest drugEvaluationWorkRequest = new DrugEvaluationWorkRequest();
            drugEvaluationWorkRequest.setDrug((Drugs) tblApprovedDrugs.getValueAt(selectedrow, 0));
            drugEvaluationWorkRequest.setDrugVersions((DrugVersions) tblApprovedDrugs.getValueAt(selectedrow, 1));
            drugEvaluationWorkRequest.setSender(account);
            drugEvaluationWorkRequest.setStatus("Sent");

            Organization organization = null;
            for (Enterprise ent : network.getEnterpriseDirectory().getEnterpriseList()) {

                if (ent instanceof FDAEnterprise) {

                    for (Organization org : ent.getOrganizationDirectory().getOrganizationList()) {
                        if (org instanceof DrugEvaluationOrganization) {
                            organization = org;
                            break;
                        }
                    }
                    break;
                }
            }

            organization.getWorkQueue().getWorkRequestList().add(drugEvaluationWorkRequest);
            account.getWorkQueue().getWorkRequestList().add(drugEvaluationWorkRequest);
        }
        populateTable();
    }//GEN-LAST:event_btnRequestActionPerformed

