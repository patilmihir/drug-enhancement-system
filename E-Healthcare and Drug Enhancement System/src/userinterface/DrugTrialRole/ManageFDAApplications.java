    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblApprovedDrugs.getModel();
        dtm.setRowCount(0);

        for (WorkRequest request : drugTrialOrganization.getWorkQueue().getWorkRequestList()) {

            if (((DrugTrialWorkRequest) request).getDrugVersions().isApproved()) {
                Object[] row = new Object[4];

                row[0] = ((DrugTrialWorkRequest) request).getDrug();
                row[1] = ((DrugTrialWorkRequest) request).getDrugVersions();
                row[2] = ((DrugTrialWorkRequest) request).getDisease();
                row[3] = checkSentStatus(((DrugTrialWorkRequest) request).getDrugVersions());

                dtm.addRow(row);
            }
        }
    }

    public String checkSentStatus(DrugVersions drugVersions) {

        Organization organization = null;
        for (Enterprise ent : network.getEnterpriseDirectory().getEnterpriseList()) {

            if (ent instanceof FDAEnterprise) {

                for (Organization org : ent.getOrganizationDirectory().getOrganizationList()) {

                    if (org instanceof DrugEvaluationOrganization) {
                        organization = org;
                        break;
                    }
                }
                break;
            }
        }

        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
            if (((DrugEvaluationWorkRequest) request).getDrugVersions() == drugVersions) {
                return "Sent";
            }
        }

        return "Not yet";
    }
